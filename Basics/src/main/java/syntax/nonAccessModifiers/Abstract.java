package syntax.nonAccessModifiers;

public abstract class Abstract {
    /*CLASS
    An abstract class cannot be used to create objects.
    To access an abstract class it must be inherited from another class.
    If the class or an attribute or a method is abstract, everything has to be abstract as well

     [Access Modifier][Non Access Modifier][class][Class Name]
     */

    /*METHOD
    Abstract attributes/methods can only be used in abstract classes
    An Abstract method does not have a body.

    [Access Modifier][Non Access Modifier][Return type][functionName()];
     */

    public abstract String toString();
}
